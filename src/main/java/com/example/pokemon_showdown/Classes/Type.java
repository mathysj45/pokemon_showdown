        public static double getMultiplier(int attackTypeId, int defenderType1,
                                           Integer defenderType2) {
            // IDs are 1-indexed in SQL; adjusted to 0-indexed for the array
            double multiplier = CHART[attackTypeId-1][defenderType1 - 1];

            // Apply secondary type multiplier if it exists
            if (defenderType2 != null && defenderType2 > 0) {
                multiplier *= CHART[attackTypeId - 1][defenderType2 - 1];
            }
            return multiplier;
        }

