    /**
     * Calculates the type multiplier by cross-referencing attack and defender types.
     * Since IDs in DB start at 1, we subtract 1 to match array indexes (0-17).
     * For double-typed monsters, we multiply the two values for a x0.25, x0.5, x2 or x4 result.
     */
    public static double getMultiplier(int attackTypeId, int defenderType1, Integer defenderType2) {
        double multiplier = CHART[attackTypeId-1][defenderType1 - 1];

        if (defenderType2 != null && defenderType2 > 0) {
            multiplier *= CHART[attackTypeId - 1][defenderType2 - 1];
        }
        return multiplier;
    }

